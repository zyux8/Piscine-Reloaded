int	ft_count_if(char**tab, int (*f)(char *))
{
	int	x;
	int	count;

	x = 0;
	count = 0;
	while (tab[x])
	{
		if (f(tab[x]) == 1)
			count++;
		x++;
	}
	return (count);
}
